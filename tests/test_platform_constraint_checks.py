"""Tests for platform constraint verification checks."""

from pathlib import Path
from unittest.mock import patch

from bakec.checks.platform_constraints import (
    _extract_platform_path,
    check_real_t_typedef,
    check_literal_suffix,
    check_no_printf,
    check_no_assert,
    check_no_stdlib,
    run_platform_constraint_checks,
)


# --- Helpers ---

CORTEX_M4 = {
    "name": "ARM Cortex-M4",
    "types": {"real_T": "float", "int_T": "int32_t"},
    "constraints": {
        "dynamic_memory": False,
        "printf_allowed": False,
        "assertions": False,
        "floating_point": "single",
    },
    "type_suffix": "f",
}

DESKTOP = {
    "name": "Desktop",
    "types": {"real_T": "double", "int_T": "int32_t"},
    "constraints": {
        "dynamic_memory": False,
        "printf_allowed": True,
        "assertions": True,
        "floating_point": "double",
    },
    "type_suffix": "",
}

BANNER_CORTEX = """\
/**
 * Generated by bakeC v0.1.0
 * Model:    models/lung_mnarx.yaml [sha256:abc123]
 * Platform: platforms/cortex_m4.yaml [sha256:def456]
 */
"""

BANNER_DESKTOP = """\
/**
 * Generated by bakeC v0.1.0
 * Model:    models/lung_mnarx.yaml [sha256:abc123]
 * Platform: platforms/desktop.yaml [sha256:789abc]
 */
"""


# --- Banner extraction ---

def test_extract_platform_path_cortex():
    assert _extract_platform_path(BANNER_CORTEX) == "platforms/cortex_m4.yaml"


def test_extract_platform_path_desktop():
    assert _extract_platform_path(BANNER_DESKTOP) == "platforms/desktop.yaml"


def test_extract_platform_path_missing():
    assert _extract_platform_path("/* no banner */") is None


# --- PLAT-001: real_T typedef ---

def test_real_t_typedef_float_correct():
    code = "typedef float real_T;"
    assert check_real_t_typedef(code, "test_types.h", CORTEX_M4) == []


def test_real_t_typedef_double_correct():
    code = "typedef double real_T;"
    assert check_real_t_typedef(code, "test_types.h", DESKTOP) == []


def test_real_t_typedef_wrong_type():
    code = "typedef double real_T;"
    results = check_real_t_typedef(code, "test_types.h", CORTEX_M4)
    assert len(results) == 1
    assert results[0].check_id == "PLAT-001"
    assert results[0].severity == "error"
    assert "double" in results[0].message
    assert "float" in results[0].message


def test_real_t_typedef_missing():
    code = "typedef int32_t int_T;"
    results = check_real_t_typedef(code, "test_types.h", CORTEX_M4)
    assert len(results) == 1
    assert results[0].check_id == "PLAT-001"


def test_real_t_typedef_skips_non_types_h():
    code = "typedef double real_T;"
    assert check_real_t_typedef(code, "test.c", CORTEX_M4) == []


# --- PLAT-002: Literal suffix ---

def test_literal_suffix_f_correct():
    code = "real_T x = 0.0f;\nreal_T y = 1.5f;"
    assert check_literal_suffix(code, "test.c", CORTEX_M4) == []


def test_literal_suffix_f_missing():
    code = "real_T x = 0.0;\nreal_T y = 1.5;"
    results = check_literal_suffix(code, "test.c", CORTEX_M4)
    assert len(results) == 2
    assert all(r.check_id == "PLAT-002" for r in results)


def test_literal_suffix_unexpected_f():
    code = "real_T x = 0.0f;"
    results = check_literal_suffix(code, "test.c", DESKTOP)
    assert len(results) == 1
    assert results[0].check_id == "PLAT-002"
    assert "unexpected" in results[0].message


def test_literal_suffix_double_correct():
    code = "real_T x = 0.0;\nreal_T y = 1.5;"
    assert check_literal_suffix(code, "test.c", DESKTOP) == []


def test_literal_suffix_skips_non_c():
    code = "real_T x = 0.0;"
    assert check_literal_suffix(code, "test.h", CORTEX_M4) == []


def test_literal_suffix_ignores_comments():
    code = "/* version 0.1 */\nreal_T x = 1.0f;"
    results = check_literal_suffix(code, "test.c", CORTEX_M4)
    assert results == []


