def test_run_checks_detects_wrong_typedef(tmp_path):
    """Detect wrong real_T on cortex_m4."""
    platforms_dir = tmp_path / "platforms"
    platforms_dir.mkdir()
    platform_yaml = platforms_dir / "cortex_m4.yaml"
    platform_yaml.write_text(
        "platform:\n"
        "  name: ARM Cortex-M4\n"
        "  types:\n"
        "    real_T: float\n"
        "  constraints:\n"
        "    dynamic_memory: false\n"
        "    printf_allowed: false\n"
        "    assertions: false\n"
        "  type_suffix: 'f'\n"
    )

    types_h = (
        "/**\n"
        " * Platform: platforms/cortex_m4.yaml [sha256:abc123]\n"
        " */\n"
        "typedef double real_T;\n"
    )
    results = run_platform_constraint_checks(types_h, "test_types.h", platforms_dir)
    plat001 = [r for r in results if r.check_id == "PLAT-001"]
    assert len(plat001) == 1
    assert plat001[0].severity == "error"


